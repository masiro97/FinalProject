CREATE TABLE `Record` (
`bikeNo` char(30) NOT NULL DEFAULT '',
`rentalDate` char(35) NOT NULL DEFAULT '',
`rentalNo` char(30) NOT NULL DEFAULT '',
`rentalName` char(20) NOT NULL DEFAULT '',
`rentalHolderNo` char(20) NOT NULL DEFAULT '',
`returnDate` char(20) NOT NULL DEFAULT '',
`returnNo` char(20) NOT NULL DEFAULT '',
`returnName` char(20) NOT NULL DEFAULT '',
`returnHolderNo` char(20) NOT NULL DEFAULT '',
`useTime` int(10) NOT NULL,
`useDistance` int(10) NOT NULL,
`stationNo` char(30) NOT NULL DEFAULT '',
FOREIGN KEY(`rentalNo`) REFERENCES Rental(`stationNo`),
FOREIGN KEY(`returnNo`) REFERENCES Rental(`stationNo`));